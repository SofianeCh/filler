char	*ft_strrev(char *str)
{
	int		i;
	int		b;
	char	tmp;

	i = 0;
	b = 0;
	while (str[i])
		i++;
	i--;
	while (b < i)
	{
		tmp = str[i];
		str[i] = str[b];
		str[b] = tmp;
		i--;
		b++;
	}
	return (str);
}
